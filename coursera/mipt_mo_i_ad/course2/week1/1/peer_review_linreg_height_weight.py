# **[4]**. –°–æ–∑–¥–∞–π—Ç–µ –≤ DataFrame *data* –Ω–æ–≤—ã–π –ø—Ä–∏–∑–Ω–∞–∫ *weight_category*, –∫–æ—Ç–æ—Ä—ã–π –±—É–¥–µ—Ç –∏–º–µ—Ç—å 3 –∑–Ω–∞—á–µ–Ω–∏—è: 1 ‚Äì –µ—Å–ª–∏ –≤–µ—Å –º–µ–Ω—å—à–µ 120 —Ñ—É–Ω—Ç–æ–≤. (~ 54 –∫–≥.), 3 - –µ—Å–ª–∏ –≤–µ—Å  –±–æ–ª—å—à–µ –∏–ª–∏ —Ä–∞–≤–µ–Ω 150 —Ñ—É–Ω—Ç–æ–≤ (~68 –∫–≥.), 2 ‚Äì –≤ –æ—Å—Ç–∞–ª—å–Ω—ã—Ö —Å–ª—É—á–∞—è—Ö. –ü–æ—Å—Ç—Ä–æ–π—Ç–µ ¬´—è—â–∏–∫ —Å —É—Å–∞–º–∏¬ª (boxplot), –¥–µ–º–æ–Ω—Å—Ç—Ä–∏—Ä—É—é—â–∏–π –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç—å —Ä–æ—Å—Ç–∞ –æ—Ç –≤–µ—Å–æ–≤–æ–π –∫–∞—Ç–µ–≥–æ—Ä–∏–∏. –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ –º–µ—Ç–æ–¥ *boxplot* –±–∏–±–ª–∏–æ—Ç–µ–∫–∏ Seaborn –∏ –º–µ—Ç–æ–¥ *apply* Pandas DataFrame. –ü–æ–¥–ø–∏—à–∏—Ç–µ –æ—Å—å *y* –º–µ—Ç–∫–æ–π ¬´–†–æ—Å—Ç¬ª, –æ—Å—å *x* ‚Äì –º–µ—Ç–∫–æ–π ¬´–í–µ—Å–æ–≤–∞—è –∫–∞—Ç–µ–≥–æ—Ä–∏—è¬ª.

# In[9]:


def weight_category(weight):
    if (weight < 120):
        return 1
    elif (weight >= 150):
        return 3
    else:
        return 2

data['weight_cat'] = data['Weight'].apply(weight_category)
