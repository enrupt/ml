# $$error(w_0, w_1) = \sum_{i=1}^n {(y_i - (w_0 + w_1 * x_i))}^2 $$
# –ó–¥–µ—Å—å $n$ ‚Äì —á–∏—Å–ª–æ –Ω–∞–±–ª—é–¥–µ–Ω–∏–π –≤ –Ω–∞–±–æ—Ä–µ –¥–∞–Ω–Ω—ã—Ö, $y_i$ –∏ $x_i$ ‚Äì —Ä–æ—Å—Ç –∏ –≤–µ—Å $i$-–æ–≥–æ —á–µ–ª–æ–≤–µ–∫–∞ –≤ –Ω–∞–±–æ—Ä–µ –¥–∞–Ω–Ω—ã—Ö. 

# In[11]:


def loss(w0, w1):
    sum = 0.0
    for i in range(1, len(data)):
        y_i = data['Height'][i]
        x_i = data['Weight'][i]
        sum += (y_i - (w0 + w1*x_i))**2
    return sum


